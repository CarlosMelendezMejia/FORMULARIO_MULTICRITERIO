-- Verificar los datos actualizados
SELECT 
    u.id,
    CONCAT(u.nombre, ' ', u.apellidos) AS nombre_completo,
    u.cargo,
    u.dependencia,
    f.nombre AS formulario_asignado
FROM usuario u
LEFT JOIN asignacion a ON u.id = a.id_usuario
LEFT JOIN formulario f ON a.id_formulario = f.id
ORDER BY u.id;